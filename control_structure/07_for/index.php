<?php

/**
 * Perulangan For
 *
 * Sintax :
 *
 * for (exp1; expr2; expr3) {
 *      # Kode yang akan diulang
 * }
 *
 * exp1 merupakan inisialisasi awal,
 *
 * exp2 adalah kondisi, kondisi ini di cek di awal perulangan,
 * jika kondisi benar, perulangan dilanjutkan
 * jika kondisi salah, perulangan berhenti
 *
 * exp3 akan dieksekusi di akhir perulangan.
 *
 * Contoh :
 */

for ($i = 1; $i < 10; $i++) {
    echo $i;
}
