	* Menggunakan fungsi dalam teori pemograman sering juga disebut dengan istilah ‘memanggil fungsi’ (calling a function). Fungsi 

	* dipanggil dengan menulis nama dari fungsi tersebut, dan di ikuti dengan argumen (jika ada).

	* Contoh penulian function call : **/

	echo "12 pangkat 2 adalah	: ".pow(12,2);
	
	// 12 pangkat 2 adalah: 144

	// fungsi pow() adalah fungsi pemangkatan matematika bawaan dari PHP. Fungsi ini membutuhkan 2 argumen, argumen pertama 

